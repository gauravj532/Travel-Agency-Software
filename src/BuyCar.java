            cl.cr[cl.ncar].price=Double.parseDouble(jTextField2.getText());
            cl.cr[cl.ncar].phc=Double.parseDouble(jTextField3.getText());
            cl.cr[cl.ncar].pkmc=Double.parseDouble(jTextField4.getText());
            cl.cr[cl.ncar].firstreading=Double.parseDouble(jTextField5.getText());
