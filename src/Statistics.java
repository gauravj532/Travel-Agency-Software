    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     int cid=Integer.parseInt(jTextField1.getText());
     int i ;
    
      jTextArea1.setText("");
     for(i=0;i<cl.ncar;i++)
     {
         if(cid==cl.cr[i].id && cl.cr[i].status!=3)
         {
             double pr=cl.cr[i].tprofit-cl.cr[i].exp;
            jTextArea1.append("CAR NAME:              "+ cl.cr[i].name+"\n");
            jTextArea1.append("\n CAR ID:             "+ cl.cr[i].id+"\n");
            jTextArea1.append("\n CAR price:          "+ cl.cr[i].price+"\n");
            jTextArea1.append("\n CAR Rent generated: "+ cl.cr[i].tprofit+"\n");
            jTextArea1.append("\n CAR Repair cost:    "+ cl.cr[i].exp+"\n");
            jTextArea1.append("\n CAR Demand:         "+ cl.cr[i].demand+"\n");
            jTextArea1.append("\n CAR no Repair:      "+ cl.cr[i].nrepair+"\n");
            jTextArea1.append("\n CAR profit:         "+pr+"\n");
            jTextArea1.append("\n CAR meter reading:  "+cl.cr[i].ereading+"\n");
            break;
                
         }
     }
     if(i==cl.ncar)
         JOptionPane.showMessageDialog(null,"Wrong ID");
    }//GEN-LAST:event_jButton1ActionPerformed

    
