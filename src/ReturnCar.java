    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cid=Integer.parseInt(jTextField1.getText());
        //System.out.println(cid);
        int i;
        double dis,ft,am,rg;
        for( i=0;i<1000;i++)
        {
            if(cid==cl.cr[i].id)
            {
                dis=Double.parseDouble(jTextField2.getText());
                ft=Double.parseDouble(jTextField3.getText());
                am=dis*cl.cr[i].pkmc>ft*cl.cr[i].phc?dis*cl.cr[i].pkmc:ft*cl.cr[i].phc;
                am=am>4*cl.cr[i].phc?am:cl.cr[i].phc;
                if(am>cl.cr[i].advance)
                    JOptionPane.showMessageDialog(null,("Rs "+ (am-cl.cr[i].advance) +" is received from user"));
                else
                    JOptionPane.showMessageDialog(null,"Rs "+(cl.cr[i].advance-am) +" is given to user" );
                
                cl.cr[i].status=0;
                cl.cr[i].tprofit=am;
                cl.cr[i].ereading+=dis;
                break;
            }
            
                
           
        }
         this.dispose();
        if(i==1000)
            JOptionPane.showMessageDialog(null,"you have entered wrong id");
        
    }//GEN-LAST:event_jButton1ActionPerformed

