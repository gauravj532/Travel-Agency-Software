    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       jTextArea1.setText("");
      // System.out.println(cl.ncar);
       for(int i=0;i<cl.ncar;i++)
       {
           if(cl.cr[i].status==0)
           {
               jTextArea1.append("Serial No" + (i+1) + "\n" );
               jTextArea1.append("Car Name:                   "+cl.cr[i].name+"\n");
               jTextArea1.append("Unique ID:                   "+cl.cr[i].id+"\n");
               jTextArea1.append("Pre hour Charges       "+cl.cr[i].phc+"\n");
               jTextArea1.append("Per KM Charges          "+cl.cr[i].pkmc+"\n");
               String s=cl.cr[i].ac==1?"AC":"Non-AC";
               jTextArea1.append("Type:                          "+s+"\n");
           }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

